"use client"

import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card"
import { Button } from "@/components/ui/button"
import { InterviewResults as IResults } from "./interview-session"
import { ProgressRing } from "@/components/dashboard/progress-ring"
import { 
  Download, 
  RotateCcw, 
  Share2, 
  CheckCircle, 
  AlertCircle,
  Clock,
  Target,
  TrendingUp,
  ChevronDown,
  ChevronUp
} from "lucide-react"
import { cn } from "@/lib/utils"
import { useState } from "react"
import Link from "next/link"

interface InterviewResultsProps {
  results: IResults
  onRetry: () => void
}

export function InterviewResults({ results, onRetry }: InterviewResultsProps) {
  const [expandedQuestion, setExpandedQuestion] = useState<number | null>(null)

  const formatTime = (seconds: number) => {
    const mins = Math.floor(seconds / 60)
    const secs = seconds % 60
    return `${mins}m ${secs}s`
  }

  const getScoreColor = (score: number) => {
    if (score >= 85) return "text-green-500"
    if (score >= 70) return "text-primary"
    if (score >= 50) return "text-yellow-500"
    return "text-red-500"
  }

  const strengths = [
    "Clear and articulate communication",
    "Good use of the STAR method",
    "Confident body language",
    "Logical problem breakdown",
  ].slice(0, Math.ceil(results.overallScore / 25))

  const improvements = [
    "Practice more technical problem solving",
    "Add specific metrics to your examples",
    "Work on time management",
    "Maintain better eye contact",
  ].slice(0, Math.ceil((100 - results.overallScore) / 25))

  const handleDownloadPDF = () => {
    // Generate mock PDF content
    const pdfContent = `
INTERMATE INTERVIEW REPORT
==========================

Interview Type: ${results.category}
Target Role: ${results.role}
Company: ${results.company || "Not specified"}
Date: ${new Date().toLocaleDateString()}
Duration: ${formatTime(results.totalTime)}

OVERALL SCORE: ${results.overallScore}%

QUESTION BREAKDOWN:
${results.questions.map((q, i) => `
${i + 1}. ${q.question}
   Difficulty: ${q.difficulty}
   Score: ${q.score}%
   Time: ${formatTime(q.timeSpent)}
   Feedback: ${q.feedback}
`).join("")}

STRENGTHS:
${strengths.map(s => `- ${s}`).join("\n")}

AREAS FOR IMPROVEMENT:
${improvements.map(i => `- ${i}`).join("\n")}

Generated by InterMate - AI-Powered Interview Preparation Platform
    `

    const blob = new Blob([pdfContent], { type: "text/plain" })
    const url = URL.createObjectURL(blob)
    const a = document.createElement("a")
    a.href = url
    a.download = `InterMate_Interview_Report_${new Date().toISOString().split("T")[0]}.txt`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
  }

  return (
    <div className="space-y-6">
      {/* Score Overview */}
      <Card className="overflow-hidden">
        <div className="bg-gradient-to-r from-primary/10 via-primary/5 to-transparent p-6">
          <div className="flex flex-col items-center gap-6 md:flex-row md:justify-between">
            <div className="flex items-center gap-6">
              <ProgressRing progress={results.overallScore} size={120} strokeWidth={10}>
                <div className="text-center">
                  <span className={cn("text-3xl font-bold", getScoreColor(results.overallScore))}>
                    {results.overallScore}
                  </span>
                  <p className="text-xs text-muted-foreground">Score</p>
                </div>
              </ProgressRing>
              <div>
                <h2 className="text-2xl font-bold">Interview Complete!</h2>
                <p className="text-muted-foreground">
                  {results.role} {results.company && `at ${results.company}`}
                </p>
                <div className="mt-2 flex items-center gap-4">
                  <span className="flex items-center gap-1 text-sm text-muted-foreground">
                    <Clock className="h-4 w-4" />
                    {formatTime(results.totalTime)}
                  </span>
                  <span className="flex items-center gap-1 text-sm text-muted-foreground">
                    <Target className="h-4 w-4" />
                    {results.questions.length} questions
                  </span>
                </div>
              </div>
            </div>
            <div className="flex gap-3">
              <Button variant="outline" size="icon" onClick={handleDownloadPDF}>
                <Download className="h-4 w-4" />
              </Button>
              <Button variant="outline" size="icon">
                <Share2 className="h-4 w-4" />
              </Button>
              <Button onClick={onRetry} className="gap-2">
                <RotateCcw className="h-4 w-4" />
                Try Again
              </Button>
            </div>
          </div>
        </div>
      </Card>

      {/* Strengths & Improvements */}
      <div className="grid gap-6 md:grid-cols-2">
        <Card>
          <CardHeader className="pb-3">
            <CardTitle className="flex items-center gap-2 text-lg text-green-500">
              <CheckCircle className="h-5 w-5" />
              Strengths
            </CardTitle>
          </CardHeader>
          <CardContent>
            <ul className="space-y-2">
              {strengths.map((strength, i) => (
                <li key={i} className="flex items-start gap-2 text-sm">
                  <TrendingUp className="mt-0.5 h-4 w-4 shrink-0 text-green-500" />
                  <span>{strength}</span>
                </li>
              ))}
            </ul>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="pb-3">
            <CardTitle className="flex items-center gap-2 text-lg text-yellow-500">
              <AlertCircle className="h-5 w-5" />
              Areas for Improvement
            </CardTitle>
          </CardHeader>
          <CardContent>
            <ul className="space-y-2">
              {improvements.map((improvement, i) => (
                <li key={i} className="flex items-start gap-2 text-sm">
                  <Target className="mt-0.5 h-4 w-4 shrink-0 text-yellow-500" />
                  <span>{improvement}</span>
                </li>
              ))}
            </ul>
          </CardContent>
        </Card>
      </div>

      {/* Question Breakdown */}
      <Card>
        <CardHeader>
          <CardTitle>Question Breakdown</CardTitle>
        </CardHeader>
        <CardContent className="space-y-3">
          {results.questions.map((question, index) => (
            <div 
              key={index}
              className="rounded-lg border border-border overflow-hidden"
            >
              <button
                className="flex w-full items-center justify-between p-4 text-left hover:bg-muted/50 transition-colors"
                onClick={() => setExpandedQuestion(expandedQuestion === index ? null : index)}
              >
                <div className="flex items-center gap-4">
                  <span className={cn(
                    "flex h-10 w-10 items-center justify-center rounded-lg text-sm font-bold",
                    question.score >= 80 ? "bg-green-500/10 text-green-500" :
                    question.score >= 60 ? "bg-primary/10 text-primary" :
                    "bg-yellow-500/10 text-yellow-500"
                  )}>
                    {question.score}%
                  </span>
                  <div>
                    <p className="font-medium line-clamp-1">{question.question}</p>
                    <div className="flex items-center gap-3 mt-1">
                      <span className={cn(
                        "text-xs font-medium",
                        question.difficulty === "easy" && "text-green-500",
                        question.difficulty === "medium" && "text-yellow-500",
                        question.difficulty === "hard" && "text-red-500"
                      )}>
                        {question.difficulty}
                      </span>
                      <span className="text-xs text-muted-foreground">
                        {formatTime(question.timeSpent)} / {formatTime(question.timeLimit)}
                      </span>
                    </div>
                  </div>
                </div>
                {expandedQuestion === index ? (
                  <ChevronUp className="h-5 w-5 text-muted-foreground" />
                ) : (
                  <ChevronDown className="h-5 w-5 text-muted-foreground" />
                )}
              </button>
              {expandedQuestion === index && (
                <div className="border-t border-border bg-muted/30 p-4">
                  <h4 className="text-sm font-medium mb-2">AI Feedback</h4>
                  <p className="text-sm text-muted-foreground">{question.feedback}</p>
                </div>
              )}
            </div>
          ))}
        </CardContent>
      </Card>

      {/* Actions */}
      <div className="flex justify-center gap-4">
        <Link href="/learning">
          <Button variant="outline" className="gap-2">
            Improve Skills
          </Button>
        </Link>
        <Link href="/peer-review">
          <Button variant="outline" className="gap-2">
            Request Peer Review
          </Button>
        </Link>
        <Link href="/reports">
          <Button variant="outline" className="gap-2">
            View All Reports
          </Button>
        </Link>
      </div>
    </div>
  )
}
